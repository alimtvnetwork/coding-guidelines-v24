function Verify-Install {
    if (-not $VerifyPairs) { return }
    $missing = @()
    foreach ($pair in $VerifyPairs.Split(",")) {
        $parts = $pair.Split("|")
        $relpath = $parts[0]
        $kind = $parts[1]
        $absPath = Join-Path $Target $relpath
        switch ($kind) {
            "file" { if (-not (Test-Path -LiteralPath $absPath -PathType Leaf))      { $missing += "file: $absPath" } }
            "dir"  { if (-not (Test-Path -LiteralPath $absPath -PathType Container)) { $missing += "dir : $absPath" } }
            default { Write-Warning "  unknown verify kind '$kind' for $relpath — skipping" }
        }
    }
    if ($missing.Count -gt 0) {
        Write-Host ""
        Write-Host "❌ Install verification FAILED — $($missing.Count) required path(s) missing:" -ForegroundColor Red
        foreach ($m in $missing) { Write-Host "     • $m" -ForegroundColor Red }
        Write-Host ""
        Write-Host "   The archive was downloaded and extracted but did NOT contain"
        Write-Host "   the expected artifacts. Common causes:"
        Write-Host "     • A pinned -Version was published before the prebuilt artifact"
        Write-Host "       was added to the release. Try re-running without -Version"
        Write-Host "       to fetch the main-branch zip."
        Write-Host "     • The release workflow failed to build the prebuilt artifact."
        Write-Host "     • A previous -Target run partially overwrote the install."
        Stop-Install -Code 4 -Message ""
    }
    $count = $VerifyPairs.Split(",").Count
    Write-Host "  ✓ verified $count required path(s) present" -ForegroundColor Green
}
