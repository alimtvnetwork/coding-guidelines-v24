__installer_on_err() {
    local rc=$?
    local line=${1:-?}
    local cmd=${2:-?}
    {
        echo ""
        echo "════════════════════════════════════════════════════════"
        echo "  ❌ splitdb-install FAILED (exit $rc) at line $line"
        echo "     command: $cmd"
        echo "  ────────────────────────────────────────────────────"
        echo "  Crash log: $__INSTALLER_LOG_FILE"
        echo "════════════════════════════════════════════════════════"
    } | tee -a "$__INSTALLER_LOG_FILE" >&2
    exit "$rc"
}
